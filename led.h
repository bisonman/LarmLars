class LED {
	//********************************************************
	public:
		LED(int pin)
		{
			ledPIN = pin;
			pinMode(ledPIN, OUTPUT);
			digitalWrite(ledPIN, HIGH);
		}
		~LED(){};

    //********************************************************
    void setAction(int mode);

		//********************************************************
		int setMode(int mode);

    //********************************************************
    int setMode(int mode, int cnt);

    //********************************************************
    void flashCount(int cnt);
    
		//********************************************************
		int getMode();
		
		//********************************************************
		void loop();
		
	//********************************************************
	private:
		int ledMode;
    int oldLedMode;
		int ledPIN;
		int oldSekund;
    int count;
};
